package com.yify.object;

public class AuthObject {

	private String hash;
	private AuthUserObject user;
	
	public String getHash() {
		return hash;
	}
	public void setHash(String hash) {
		this.hash = hash;
	}
	public AuthUserObject getUser() {
		return user;
	}
	public void setUser(AuthUserObject user) {
		this.user = user;
	}
	
}


